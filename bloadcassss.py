def banner(title, color=CYAN):
    line = "═" * 44
    print(color + f"╔{line}╗" + RESET)
    print(color + f"║ {BOLD}{title:<42}{RESET}{color}║" + RESET)
    print(color + f"╚{line}╝" + RESET)

def print_group(title, items, color_title, color_item):
    banner(title, color_title)
    for i, (k, v) in enumerate(items.items(), start=1):
        bullet = "▸" if i % 2 else "▪"
        shade = color_item if i % 2 else WHITE
        print(shade + f"{bullet} {k:<2} - {v}" + RESET)
    print(SEP)

